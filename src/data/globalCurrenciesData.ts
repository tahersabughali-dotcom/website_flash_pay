import type { GlobalCurrency } from "@/types/currency";

const noteDefault = {
  ar: "عرض العملة في الدليل لا يعني توفر سعر أو تنفيذ فوري.",
  en: "Listing a currency does not mean instant pricing or execution availability.",
};

function fiat(
  code: string,
  name: { ar: string; en: string },
  symbol: string,
  order: number,
  options?: Partial<GlobalCurrency>,
): GlobalCurrency {
  const lower = code.toLowerCase();
  return {
    id: code,
    code,
    symbol,
    name,
    countries: options?.countries ?? [],
    type: "fiat",
    iconImage: `/icons/currencies/${["usd", "eur", "gbp", "egp", "try", "cny", "ils"].includes(lower) ? lower : "default"}.svg`,
    status: options?.status ?? "available_by_request",
    isFeatured: options?.isFeatured ?? false,
    order,
    notes: options?.notes ?? noteDefault,
    ...options,
  };
}

